package food_delivery;
/**
 * DeliveryTracker interface implements Realtime,Estimated classes
 */
interface DeliveryTracker{
	/**
	 * startDelivery method logic is written in the classes that implements DeliveryTracker interface 
	 */
	public void startDelivery();
	/**
	 * trackProgress method logic is written in the classes that implements DeliveryTracker interface 
	 */
	public void trackProgress();
	/**
	 * completeDelivery method logic is written in the classes that implements DeliveryTracker interface 
	 */
	public void completeDelivery();
}

